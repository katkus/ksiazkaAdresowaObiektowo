{
    if(uzytkownikMenedzer.czyUzytkownikJestZalogowany())
    {
      adresatMenedzer -> dodajAdresata();
    }
    else
    {
        cout << "Aby dodac adresata, nalezy najpierw sie zalogowac." << endl;
        system("read"); // Windows system("pause")
    }
}
